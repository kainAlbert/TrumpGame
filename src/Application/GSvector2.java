package Application;

// xとyを持つ構造体クラス
public class GSvector2 {

	public double x;
	public double y;

	public GSvector2(){
		x = 0;
		y = 0;
	}

	public GSvector2( double x, double y ){
		this.x = x;
		this.y = y;
	}

	public GSvector2( int x, int y ){
		this.x = x;
		this.y = y;
	}
}
